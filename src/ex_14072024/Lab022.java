package ex_14072024;

public class Lab022 {
    public static void main(String[] args) {
        //  Ternary operator.
        //  ? if true do this : ele do that
        int result = 18>38 ? 10 : 20;
        System.out.println(result);
        // Max between two numbers
        int a= 10;
        int b= 20;
        int max = a > b ? a : b;
        int min = a < b ? a : b;
        System.out.println(max);
        System.out.println(min);
    }
}
