public class Lab062 {
    public static void main(String[] args) {
        // even odd program
        for (int i = 0; i <50; i++) {
            if (i%2==0){
                System.out.println("Even number"+i);
            }else {
                System.out.println("Odd number"+i);
            }

        }

    }
}
