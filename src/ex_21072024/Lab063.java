package ex_21072024;

public class Lab063 {
    public static void main(String[] args) {
        // Loops - while and do while
        // while - A, B, C
        //         // A - Init
        //        // B - Condition
        //        // C - Increment / Decrement

        int i=0;
        while (i<=10){
            System.out.println("Its me Inside the while loop :"+i);
            i++;
        }
        for (int j = 0; j < 10; j++) {
            System.out.println("inside the for loop :"+j);

        }
    }
}
