package July.ex_13072024;

public class Lab007 {
    public static void main(String[] args) {
        String name = "Jaya";
        int age =35;
        // Formatting
        // \n means new line
        // %d ->  any integer - byte, short, int or long
        // %s -> String
        // %c -> char
        // %f -> float
        System.out.printf("My name is %s\n",name);
        System.out.printf("The age is %d",age);
    }
}
